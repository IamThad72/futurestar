SET asset_category = CASE classification_type
    WHEN 'Household Asset' THEN 'Asset'
    WHEN 'Primary residence' THEN 'Real Estate'
    WHEN 'Vacation home' THEN 'Real Estate'
    WHEN 'Rental property' THEN 'Real Estate'
    WHEN 'Undeveloped land/lot' THEN 'Real Estate'
    WHEN 'Condominium' THEN 'Real Estate'
    WHEN 'Timeshare' THEN 'Real Estate'
    WHEN 'Home equity/ownership interest' THEN 'Liability'
    WHEN 'Household furnishings' THEN 'Asset'
    WHEN 'Living room furniture' THEN 'Asset'
    WHEN 'Bedroom furniture' THEN 'Asset'
    WHEN 'Dining room furniture' THEN 'Asset'
    WHEN 'Home office furniture' THEN 'Asset'
    WHEN 'Major appliances (refrigerator/oven/washer/dryer)' THEN 'Asset'
    WHEN 'Small kitchen appliances' THEN 'Asset'
    WHEN 'Electronics (TVs/computers/tablets)' THEN 'Asset'
    WHEN 'Home theater/audio equipment' THEN 'Asset'
    WHEN 'Phones and accessories' THEN 'Asset'
    WHEN 'Cameras and photo equipment' THEN 'Asset'
    WHEN 'Jewelry' THEN 'Asset'
    WHEN 'Watches' THEN 'Asset'
    WHEN 'Handbags/accessories (high-value)' THEN 'Asset'
    WHEN 'Artwork' THEN 'Asset'
    WHEN 'Antiques/collectibles' THEN 'Asset'
    WHEN 'Precious metals (gold/silver/coins)' THEN 'Asset'
    WHEN 'Firearms' THEN 'Asset'
    WHEN 'Tools and equipment' THEN 'Asset'
    WHEN 'Lawn/garden equipment' THEN 'Asset'
    WHEN 'Power tools' THEN 'Asset'
    WHEN 'Outdoor furniture' THEN 'Asset'
    WHEN 'Grill/smoker' THEN 'Asset'
    WHEN 'Sporting goods' THEN 'Asset'
    WHEN 'Musical instruments' THEN 'Asset'
    WHEN 'Hobby collections (stamps/cards/models)' THEN 'Asset'
    WHEN 'Books and libraries' THEN 'Asset'
    WHEN 'Vehicles (cars/trucks/SUVs)' THEN 'Vehicle'
    WHEN 'Motorcycle' THEN 'Vehicle'
    WHEN 'Boat/watercraft' THEN 'Vehicle'
    WHEN 'Trailer' THEN 'Vehicle'
    WHEN 'RV/camper' THEN 'Vehicle'
    WHEN 'Golf cart/ATV/UTV' THEN 'Vehicle'
    WHEN 'Bicycles/e-bikes' THEN 'Vehicle'
    WHEN 'Vehicle titles/registrations' THEN 'Vehicle'
    WHEN 'Cash on hand' THEN 'Cash'
    WHEN 'Checking account' THEN 'Cash'
    WHEN 'Savings account' THEN 'Cash'
    WHEN 'Money market account' THEN 'Cash'
    WHEN 'Certificates of deposit (CDs)' THEN 'Cash'
    WHEN 'Brokerage account (taxable)' THEN 'Cash'
    WHEN 'Stocks' THEN 'Investment'
    WHEN 'Bonds' THEN 'Asset'
    WHEN 'Mutual funds' THEN 'Asset'
    WHEN 'ETFs' THEN 'Asset'
    WHEN 'Treasury securities' THEN 'Asset'
    WHEN 'I Bonds' THEN 'Asset'
    WHEN 'Retirement accounts (401(k)/403(b)/457)' THEN 'Asset'
    WHEN 'Traditional IRA' THEN 'Asset'
    WHEN 'Roth IRA' THEN 'Asset'
    WHEN 'Pension benefits' THEN 'Asset'
    WHEN 'Annuities' THEN 'Asset'
    WHEN 'Life insurance policy (cash value/benefit)' THEN 'Insurance'
    WHEN 'Health savings account (HSA)' THEN 'Investment'
